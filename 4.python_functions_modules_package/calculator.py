# user defined module


# addition
def add(num1, num2):
    result = num1 + num2
    print('Addition = ', result)
    return result


# subtraction
def sub(num1, num2):
    result = num1 - num2
    print('subtraction = ', result)
    return result


#  multiplication
def mul(num1, num2):
    result = num1 * num2
    print('multiplication = ', result)
    return result


# division
def div(num1, num2):
    result = num1 / num2
    print('division = ', result)
    return result
